// Generates src/osm/poiIconBBoxes.ts: for every poiIcon, the drawing's
// bounding box (so markers can render the *drawing* at a consistent size
// instead of scaling the whole canvas — each icon's canvas has idiosyncratic
// padding). Uses @resvg/resvg-js (a plain npm dep, no system tools) to measure
// the ink bbox. The emitted .ts is committed, so normal builds don't run this.
//
// Run: node scripts/gen-poi-icon-bboxes.mjs  (or: pnpm gen-poi-icon-bboxes)
//
// Output value per icon: [lx, ly, bw, bh, vbW, vbH] where (lx,ly,bw,bh) is the
// drawing bbox expressed in viewport-local units (origin at the viewport's top
// left) and (vbW,vbH) is the viewport size. See src/shared/poiIconGlyph.ts for
// how the marker turns this into a placement rect.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Resvg } from '@resvg/resvg-js';

const here = dirname(fileURLToPath(import.meta.url));
const ICON_DIR = join(here, '..', 'src', 'images', 'poiIcons');
const OUT_FILE = join(here, '..', 'src', 'osm', 'poiIconBBoxes.ts');

function walk(dir) {
  const out = [];
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, e.name);
    if (e.isDirectory()) out.push(...walk(p));
    else if (e.name.endsWith('.svg')) out.push(p);
  }
  return out;
}

// Round to 3 decimals, dropping trailing zeros.
const r = (n) => Number(n.toFixed(3));

// The icon's viewport (viewBox, else width/height), parsed from the <svg> tag —
// resvg's bbox is in this coordinate system, so we subtract its origin to get
// viewport-local coords.
function viewport(svgTag) {
  const vb = svgTag.match(/viewBox\s*=\s*"([^"]+)"/);
  if (vb) {
    const [x, y, w, h] = vb[1]
      .trim()
      .split(/[\s,]+/)
      .map(Number);
    return { x, y, w, h };
  }
  const w = svgTag.match(/\bwidth\s*=\s*"([\d.]+)/);
  const h = svgTag.match(/\bheight\s*=\s*"([\d.]+)/);
  if (w && h) return { x: 0, y: 0, w: Number(w[1]), h: Number(h[1]) };
  return null;
}

const ok = [];
const failed = [];
for (const abs of walk(ICON_DIR).sort()) {
  const src = readFileSync(abs, 'utf8');
  const vp = viewport(src.match(/<svg\b[^>]*>/)?.[0] ?? '');
  if (!vp || !vp.w || !vp.h) {
    failed.push({ abs, error: 'no viewport (viewBox/width/height)' });
    continue;
  }

  let bbox;
  try {
    // loadSystemFonts:false skips scanning the system font dirs on every
    // instance (these icons have no text) — that scan dominated the runtime.
    bbox = new Resvg(src, { font: { loadSystemFonts: false } }).getBBox();
  } catch (e) {
    failed.push({ abs, error: `resvg failed: ${String(e.message).trim()}` });
    continue;
  }
  if (!bbox || !(bbox.width > 0) || !(bbox.height > 0)) {
    failed.push({ abs, error: 'empty bbox' });
    continue;
  }

  ok.push({
    abs,
    bbox: [
      r(bbox.x - vp.x),
      r(bbox.y - vp.y),
      r(bbox.width),
      r(bbox.height),
      r(vp.w),
      r(vp.h),
    ],
  });
}

const importLines = [];
const entryLines = [];
ok.forEach((res, idx) => {
  // import path relative to src/osm/, matching osmTagToIconMapping's style.
  const rel = relative(join(here, '..', 'src', 'osm'), res.abs).replaceAll(
    '\\',
    '/',
  );
  // JSON.stringify double-quotes the path so filenames with apostrophes
  // (e.g. forester's_lodge.svg) don't break the import string.
  importLines.push(`import i${idx} from ${JSON.stringify(rel)};`);
  entryLines.push(`  [i${idx}]: [${res.bbox.join(', ')}],`);
});

writeFileSync(
  OUT_FILE,
  `// AUTO-GENERATED by scripts/gen-poi-icon-bboxes.mjs — do not edit by hand.
// Drawing bounding box per poiIcon, keyed by its imported asset URL.
// Value: [lx, ly, bw, bh, vbW, vbH] — drawing bbox in viewport-local units
// (lx,ly,bw,bh) plus the viewport size (vbW,vbH). See the generator header.

${importLines.join('\n')}

export type PoiIconBBox = readonly [
  lx: number,
  ly: number,
  bw: number,
  bh: number,
  vbW: number,
  vbH: number,
];

export const poiIconBBoxes: Record<string, PoiIconBBox> = {
${entryLines.join('\n')}
};
`,
);

console.log(`Wrote ${OUT_FILE}: ${ok.length} icons.`);
if (failed.length) {
  console.warn(
    `\n${failed.length} icon(s) SKIPPED (will fall back to full-canvas render):`,
  );
  for (const f of failed)
    console.warn(`  - ${relative(ICON_DIR, f.abs)}: ${f.error}`);
}
